public class Lista2_Exerc14 {

    public static void main(String[] args) {
        
       int[] vetor = new int[50];
       int i = 0;

       while (i <= 50){
        vetor[i] = 0;
        System.out.println(vetor[i]);
        i++;
       }
    }
    
}


