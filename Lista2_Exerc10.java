public class Lista2_Exerc10{
    public static void main(String[] args) {
        
    
     int i = 2;
     
   
     while (i <= 100){
           if(i % 2 == 0){
            System.out.print(i + " ");
           }
         
            i = i + 1;
        }
    
    }

}

